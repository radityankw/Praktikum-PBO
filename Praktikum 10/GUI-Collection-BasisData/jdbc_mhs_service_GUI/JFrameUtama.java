    private void loadAllData() {
        modelNama.clear();
        modelId.clear();
        List<Mahasiswa> list = service.getAll();
        for (Mahasiswa m : list) {
            modelId.addElement(String.valueOf(m.getId()));
            modelNama.addElement(m.getName());
        }
    }
    
    private void updateTextFieldsFromList() {
        int selectedIndex = jListID.getSelectedIndex();

        if (selectedIndex >= 0 && selectedIndex < modelId.size()) {
            String selectedId = modelId.getElementAt(selectedIndex);
            String selectedNama = modelNama.getElementAt(selectedIndex);

            jTextFieldIDForEdit.setText(selectedId);
            jTextFieldNamaForEdit.setText(selectedNama);
            jTextFieldIDForHapus.setText(selectedId);
            jTextFieldNamaForHapus.setText(selectedNama);
        }
    }
    
    private void sinkronkanPilihanList() {
        jListID.addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int index = jListID.getSelectedIndex();
                jListNama.setSelectedIndex(index);
            }
        });

        jListNama.addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int index = jListNama.getSelectedIndex();
                jListID.setSelectedIndex(index);
            }
        });
    }


