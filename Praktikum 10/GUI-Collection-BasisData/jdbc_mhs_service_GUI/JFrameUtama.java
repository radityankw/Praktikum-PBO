    private void jButtonTambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahkanActionPerformed
        String nama = jTextFieldIsiNama.getText();
        if (!nama.isEmpty()) {
            Mahasiswa mhs = new Mahasiswa();
            mhs.setName(nama);
            // Cari ID tertinggi + 1
            List<Mahasiswa> list = service.getAll();
            int nextId = 1;
            for (Mahasiswa m : list) {
                if (m.getId() >= nextId) nextId = m.getId() + 1;
            }
            mhs.setId(nextId);
            service.add(mhs);
            loadAllData();
        }
    }//GEN-LAST:event_jButtonTambahkanActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        try {
            int id = Integer.parseInt(jTextFieldIDForEdit.getText());
            String nama = jTextFieldNamaForEdit.getText();
            Mahasiswa mhs = new Mahasiswa(id, nama);
            service.update(mhs);
            loadAllData();
        } catch (NumberFormatException e) {
            System.out.println("ID tidak valid");
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        try {
            int id = Integer.parseInt(jTextFieldIDForHapus.getText());
            service.delete(id);
            loadAllData();
        } catch (NumberFormatException e) {
            System.out.println("ID tidak valid");
        }
    }//GEN-LAST:event_jButtonHapusActionPerformed

